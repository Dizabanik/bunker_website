// Rules based on player count
// Returns how many cards to open in [Round 1, Round 2, Round 3, Round 4+]
export const getRevealSchedule = (playerCount: number) => {
    if (playerCount <= 6) return [3, 3, 2]; // 1-6
    if (playerCount <= 8) return [3, 3, 1, 1]; // 7-8
    if (playerCount <= 10) return [3, 2, 1, 1]; // 9-10
    if (playerCount <= 12) return [2, 2, 1, 1]; // 11-12
    return [2, 1, 1, 1]; // 13+
};

